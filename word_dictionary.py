def correct_words(text: str, language: str) -> str:
    """
    Check extracted text against dictionary and correct common OCR errors
    
    Args:
        text (str): The extracted text to correct
        language (str): Language code ('ne' for Nepali, 'si' for Sinhala)
    
    Returns:
        str: Text with corrections applied
    """
    
    if language == "ne":
        corrections = NEPALI_WORD_CORRECTIONS
    elif language == "si":
        corrections = SINHALA_WORD_CORRECTIONS
    else:
        return text  # Return original if language not supported
    
    corrected_text = text
    corrections_made = 0
    
    # Apply word-by-word corrections
    for wrong_word, correct_word in corrections.items():
        if wrong_word in corrected_text:
            corrected_text = corrected_text.replace(wrong_word, correct_word)
            corrections_made += 1
    
    print(f"OCR Corrections applied: {corrections_made} words corrected")
    return corrected_text



